    private void jButtonLimparNumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimparNumActionPerformed
        // TODO add your handling code here:
        int tamanho = jTextAreaVisor.getText().length();
        int num = jTextAreaVisor.getText().length() - 1;
        String novoNum;
        
        if(tamanho > 0){
            StringBuilder apagar = new StringBuilder(jTextAreaVisor.getText());
            apagar.deleteCharAt(num);
            novoNum = apagar.toString();
            jTextAreaVisor.setText(novoNum);
        }
        
    }//GEN-LAST:event_jButtonLimparNumActionPerformed

